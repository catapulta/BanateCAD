function normrgba(c,a)
	return {c[1]/255, c[2]/255, c[3]/255, a};
end

function crayola.rgba(name, a)
	local c = crayola[name];
	if c == nil then
		return {0,0,0,0};
	end

	return normrgba(c, a);
end

function crayola.rgb(name)
	return crayola.rgba(name, 1);
end

